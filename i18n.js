  let currentLanguage = FALLBACK_LANGUAGE;

  function normalizeLanguage(language) {
    if (!language || typeof language !== 'string') {
      return FALLBACK_LANGUAGE;
    }

    const normalized = language.replace('-', '_').toLowerCase();
    // tlh (Klingon) is an explicit opt-in easter egg -- match it before the
    // prefix rules so selecting it doesn't silently fall through to English.
    if (normalized === 'tlh' || normalized.startsWith('tlh')) return 'tlh';
    if (normalized.startsWith('es')) return 'es';
    if (normalized.startsWith('zh')) return 'zh_CN';
    if (normalized.startsWith('hi')) return 'hi';
    if (normalized.startsWith('ar')) return 'ar';
    return 'en';
  }

  function interpolate(template, params = {}) {
    return String(template).replace(/\{(\w+)\}/g, (_, key) => {
      return Object.prototype.hasOwnProperty.call(params, key) ? params[key] : `{${key}}`;
    });
  }

  function t(key, params = {}) {
    const bundle = translations[currentLanguage] || translations[FALLBACK_LANGUAGE];
    const fallbackBundle = translations[FALLBACK_LANGUAGE];
    const template = bundle[key] || fallbackBundle[key] || key;
    return interpolate(template, params);
  }

  function applyTranslations(root = document) {
    if (!root || !root.querySelectorAll) return;

    root.querySelectorAll('[data-i18n]').forEach((element) => {
      element.textContent = t(element.dataset.i18n);
    });

    root.querySelectorAll('[data-i18n-placeholder]').forEach((element) => {
      element.setAttribute('placeholder', t(element.dataset.i18nPlaceholder));
    });

    root.querySelectorAll('[data-i18n-title]').forEach((element) => {
      element.setAttribute('title', t(element.dataset.i18nTitle));
    });
  }

  function setLanguage(language) {
    currentLanguage = normalizeLanguage(language);
    if (typeof document !== 'undefined') {
      const metadata = SUPPORTED_LANGUAGES[currentLanguage] || SUPPORTED_LANGUAGES[FALLBACK_LANGUAGE];
      document.documentElement.lang = metadata.lang;
      document.documentElement.dir = metadata.dir;
    }
    applyTranslations(document);
    return currentLanguage;
  }

  function detectLanguage() {
    const runtimeLanguage = global.chrome?.i18n?.getUILanguage?.();
    const browserLanguage = global.navigator?.language;
    return normalizeLanguage(runtimeLanguage || browserLanguage || FALLBACK_LANGUAGE);
  }

  global.NostalgiaI18n = {
    detectLanguage,
    getLanguages: () => SUPPORTED_LANGUAGES,
    getLanguage: () => currentLanguage,
    setLanguage,
    t,
    applyTranslations
  };
